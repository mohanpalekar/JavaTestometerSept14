package javaPractice;

public class SwitchCase {

	public static void main(String[] args) {

		char j = 'z' ;

		switch(j) {
		case 'a': 
			System.out.println("j is "+j);
			break;

		case 'e':
			System.out.println("j is "+j);
			break;

		case 'd':
			System.out.println("j is "+j);
			break;

		default:
			System.out.println("j is "+j);
		}

	}

}
